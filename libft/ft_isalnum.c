#include "libft.h"

int		ft_isalnum(int c)
{
	if (((char)c >= 48) && ((char)c <= 57))
		return (1);
	else if (((char)c >= 65) && ((char)c <= 90))
		return (1);
	else if (((char)c >= 97) && ((char)c <= 122))
		return (1);
	else
		return (0);
}
