#include "libft.h"

char	*ft_tolower(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (!(str[i] >= 'A' && str[i] <= 'Z'))
			i++;
		if (!(str[i] >= 'a' && str[i] <= 'z'))
			i++;
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] = str[i] + 32;
		else
			i++;
	}
	return (str);
}
